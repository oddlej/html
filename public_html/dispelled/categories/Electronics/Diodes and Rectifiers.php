<h1>Diodes and Rectifiers</h1>
<p>Diodes are semiconductor devices that allow current to flow in one direction only. Rectifiers are circuits that convert AC to DC using diodes.</p>

<h2>Diode Characteristics</h2>
<p>Diodes have two terminals: the anode and the cathode. Current flows from the anode to the cathode when the diode is forward-biased.</p>

<h2>Rectifier Types</h2>
<ul>
    <li><strong>Half-Wave Rectifier:</strong> Uses a single diode to convert AC to DC.</li>
    <li><strong>Full-Wave Rectifier:</strong> Uses multiple diodes to convert AC to DC more efficiently.</li>
</ul>

<h2>Example Calculation</h2>
<p>Consider a half-wave rectifier with an input AC voltage of 12V RMS:</p>
<pre>
<code class="language-plaintext">
V_DC = V_RMS * 0.45
V_DC = 12V * 0.45
V_DC = 5.4V
</code>
</pre>

<h2>Applications of Diodes and Rectifiers</h2>
<p>Diodes and rectifiers are used in:</p>
<ul>
    <li>Power supplies</li>
    <li>Signal demodulation</li>
    <li>Voltage regulation</li>
</ul>

