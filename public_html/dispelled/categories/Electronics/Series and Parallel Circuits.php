<h1>Series and Parallel Circuits</h1>
<p>Understanding series and parallel circuits is essential for analyzing and designing electrical systems. These configurations determine how components are connected and how current and voltage behave in the circuit.</p>

<h2>Series Circuits</h2>
<p>In a series circuit, components are connected end-to-end, so there is only one path for current to flow.</p>

<h3>Characteristics of Series Circuits</h3>
<ul>
    <li>Current is the same through all components.</li>
    <li>Total resistance is the sum of individual resistances.</li>
    <li>Voltage is divided among components.</li>
</ul>

<h2>Parallel Circuits</h2>
<p>In a parallel circuit, components are connected across the same two points, creating multiple paths for current to flow.</p>

<h3>Characteristics of Parallel Circuits</h3>
<ul>
    <li>Voltage is the same across all components.</li>
    <li>Total resistance is less than the smallest individual resistance.</li>
    <li>Current is divided among components.</li>
</ul>

<h2>Example Calculation</h2>
<p>Consider a series circuit with resistors R1 = 2Ω, R2 = 3Ω, and R3 = 5Ω:</p>
<pre>
<code class="language-plaintext">
R_total = R1 + R2 + R3
R_total = 2Ω + 3Ω + 5Ω
R_total = 10Ω
</code>
</pre>

<h2>Applications of Series and Parallel Circuits</h2>
<p>These configurations are used in:</p>
<ul>
    <li>Designing electrical systems</li>
    <li>Analyzing circuit behavior</li>
    <li>Troubleshooting electrical problems</li>
</ul>

