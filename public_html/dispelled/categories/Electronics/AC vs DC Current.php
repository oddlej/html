<h1>AC vs. DC Current</h1>
<p>Understanding the difference between alternating current (AC) and direct current (DC) is fundamental in electrical engineering. Both types of current have unique characteristics and applications.</p>

<h2>Direct Current (DC)</h2>
<p>DC is a type of current that flows in one direction only. It is commonly used in batteries and electronic devices.</p>

<h3>Characteristics of DC</h3>
<ul>
    <li>Constant voltage and current</li>
    <li>Flows in one direction</li>
    <li>Used in low-voltage applications</li>
</ul>

<h2>Alternating Current (AC)</h2>
<p>AC is a type of current that periodically reverses direction. It is commonly used in power distribution systems.</p>

<h3>Characteristics of AC</h3>
<ul>
    <li>Variable voltage and current</li>
    <li>Reverses direction periodically</li>
    <li>Used in high-voltage applications</li>
</ul>

