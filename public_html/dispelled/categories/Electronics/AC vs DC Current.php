<h2>Applications of AC and DC</h2>
<p>AC and DC are used in:</p>
<ul>
    <li>Power distribution (AC)</li>
    <li>Electronic devices (DC)</li>
    <li>Renewable energy systems (both AC and DC)</li>
</ul>

