<h1>Lydian Mode</h1>
<p>The Lydian mode is the fourth mode of the major scale. It has a bright and dreamy sound, characterized by a raised fourth degree that gives it a distinctive, almost ethereal quality.</p>

<h2>Structure of the Lydian Mode</h2>
<p>The Lydian mode follows the pattern of whole steps (W) and half steps (H) as follows:</p>
<pre>
<code class="language-plaintext">
W - W - W - H - W - W - H
</code>
</pre>

<h2>Example: F Lydian Mode</h2>
<p>The F Lydian mode consists of the following notes:</p>
<pre>
<code class="language-plaintext">
F - G - A - B - C - D - E - F
</code>
</pre>

<h2>Sound and Character</h2>
<p>The Lydian mode has a bright and dreamy sound, characterized by the raised fourth degree. It is often used in film scores, jazz, and progressive rock to create a sense of wonder and exploration. The major third and augmented fourth intervals contribute to its distinctive character.</p>

<h2>Applications of the Lydian Mode</h2>
<p>The Lydian mode is used in various musical contexts, including:</p>
<ul>
    <li>Melody: Creating bright and dreamy melodies.</li>
    <li>Harmony: Building major chords with an augmented fourth degree.</li>
    <li>Improvisation: Soloing over major chord progressions with an augmented fourth degree.</li>
</ul>

