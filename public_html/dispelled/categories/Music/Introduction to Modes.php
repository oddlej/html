<h1>Introduction to Modes</h1>
<p>In music theory, modes are scales derived from the major scale by starting on different scale degrees. Each mode has a unique sound and character, making them useful for various musical contexts. There are seven modes, each corresponding to a different degree of the major scale.</p>

<h2>The Seven Modes</h2>
<p>The seven modes are:</p>
<ul>
    <li><strong>Ionian</strong> (1st degree)</li>
    <li><strong>Dorian</strong> (2nd degree)</li>
    <li><strong>Phrygian</strong> (3rd degree)</li>
    <li><strong>Lydian</strong> (4th degree)</li>
    <li><strong>Mixolydian</strong> (5th degree)</li>
    <li><strong>Aeolian</strong> (6th degree)</li>
    <li><strong>Locrian</strong> (7th degree)</li>
</ul>

<h2>Mode Formulas</h2>
<p>Each mode has a specific pattern of whole steps (W) and half steps (H). Here are the formulas for each mode:</p>
<ul>
    <li><strong>Ionian</strong>: W - W - H - W - W - W - H</li>
    <li><strong>Dorian</strong>: W - H - W - W - W - H - W</li>
    <li><strong>Phrygian</strong>: H - W - W - W - H - W - W</li>
    <li><strong>Lydian</strong>: W - W - W - H - W - W - H</li>
    <li><strong>Mixolydian</strong>: W - W - H - W - W - H - W</li>
    <li><strong>Aeolian</strong>: W - H - W - W - H - W - W</li>
    <li><strong>Locrian</strong>: H - W - W - H - W - W - W</li>
</ul>

<h2>Example: C Major Scale Modes</h2>
<p>Let's explore the modes derived from the C major scale:</p>

<h3>Ionian Mode (C Ionian)</h3>
<p>The Ionian mode is the same as the major scale. The notes are:</p>
<pre>
<code class="language-plaintext">
C - D - E - F - G - A - B - C
</code>
</pre>

<h3>Dorian Mode (D Dorian)</h3>
<p>The Dorian mode starts on the second degree of the major scale. The notes are:</p>
<pre>
<code class="language-plaintext">
D - E - F - G - A - B - C - D
</code>
</pre>

<h3>Phrygian Mode (E Phrygian)</h3>
<p>The Phrygian mode starts on the third degree of the major scale. The notes are:</p>
<pre>
<code class="language-plaintext">
E - F - G - A - B - C - D - E
</code>
</pre>

<h3>Lydian Mode (F Lydian)</h3>
<p>The Lydian mode starts on the fourth degree of the major scale. The notes are:</p>
<pre>
<code class="language-plaintext">
F - G - A - B - C - D - E - F
</code>
</pre>

<h3>Mixolydian Mode (G Mixolydian)</h3>
<p>The Mixolydian mode starts on the fifth degree of the major scale. The notes are:</p>
<pre>
<code class="language-plaintext">
G - A - B - C - D - E - F - G
</code>
</pre>

<h3>Aeolian Mode (A Aeolian)</h3>
<p>The Aeolian mode is the same as the natural minor scale. The notes are:</p>
<pre>
<code class="language-plaintext">
A - B - C - D - E - F - G - A
</code>
</pre>

<h3>Locrian Mode (B Locrian)</h3>
<p>The Locrian mode starts on the seventh degree of the major scale. The notes are:</p>
<pre>
<code class="language-plaintext">
B - C - D - E - F - G - A - B
</code>
</pre>

<h2>Applications of Modes</h2>
<p>Modes are used in various musical contexts, including:</p>
<ul>
    <li>Composition: Creating melodies and harmonies with different modal flavors.</li>
    <li>Improvisation: Providing a framework for soloing over chord progressions.</li>
    <li>Analysis: Understanding the modal structure of existing music.</li>
</ul>

<h2>Practice Exercises</h2>
<p>Here are some practice exercises to help you get familiar with modes:</p>
<ol>
    <li>Play each mode of the C major scale on your instrument, ascending and descending.</li>
    <li>Write out the notes of each mode in different keys and play them on your instrument.</li>
    <li>Practice improvising using different modes over a static chord or drone.</li>
</ol>

