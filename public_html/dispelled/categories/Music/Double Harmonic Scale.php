<h1>Double Harmonic Scale</h1>
<p>The double harmonic scale, also known as the Byzantine scale, is a seven-note scale that has a unique, exotic sound. It is characterized by its two augmented seconds and is often used in Middle Eastern, Indian, and Eastern European music.</p>

<h2>Structure of the Double Harmonic Scale</h2>
<p>The double harmonic scale follows the pattern of whole steps (W), half steps (H), and augmented seconds (A):</p>
<pre>
<code class="language-plaintext">
H - A - H - W - H - A - H
</code>
</pre>

<h2>Example: C Double Harmonic Scale</h2>
<p>The C double harmonic scale consists of the following notes:</p>
<pre>
<code class="language-plaintext">
C - D♭ - E - F - G - A♭ - B - C
</code>
</pre>

<h2>Sound and Character</h2>
<p>The double harmonic scale has a unique, exotic sound due to its two augmented seconds. It is often used to create a sense of mystery and drama in music.</p>

<h2>Applications of the Double Harmonic Scale</h2>
<p>The double harmonic scale is used in various musical contexts, including:</p>
<ul>
    <li>Melody: Creating exotic and dramatic melodies.</li>
    <li>Harmony: Adding tension and intensity to chord progressions.</li>
    <li>Improvisation: Soloing with an exotic, Middle Eastern sound.</li>
</ul>

<h2>Chords Derived from the Double Harmonic Scale</h2>
<p>The double harmonic scale can be used to derive the following chords:</p>
<ul>
    <li>Major chords (e.g., C)</li>
    <li>Minor chords (e.g., D♭m)</li>
    <li>Augmented chords (e.g., E+)</li>
    <li>Diminished chords (e.g., G°)</li>
</ul>

