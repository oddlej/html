<h1>Melodic Minor Scale</h1>
<p>The melodic minor scale is a variation of the natural minor scale with raised sixth and seventh degrees when ascending, and it reverts to the natural minor scale when descending. This creates a smooth melodic line when ascending and a more traditional minor sound when descending.</p>

<h2>Structure of the Melodic Minor Scale</h2>
<p>The melodic minor scale follows different patterns when ascending and descending:</p>
<pre>
<code class="language-plaintext">
Ascending: W - H - W - W - W - W - H
Descending: W - W - H - W - W - H - W
</code>
</pre>

