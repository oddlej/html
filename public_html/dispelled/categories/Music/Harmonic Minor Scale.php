<h1>Harmonic Minor Scale</h1>
<p>The harmonic minor scale is a variation of the natural minor scale with a raised seventh degree. This raised seventh creates a leading tone that gives the scale a distinctive, exotic sound. It is widely used in classical, jazz, and metal music.</p>

<h2>Structure of the Harmonic Minor Scale</h2>
<p>The harmonic minor scale follows the pattern of whole steps (W) and half steps (H) as follows:</p>
<pre>
<code class="language-plaintext">
W - H - W - W - H - W+H - H
</code>
</pre>

