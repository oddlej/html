<h1>Augmented Scale</h1>
<p>The augmented scale, also known as the symmetric scale, is a six-note scale that alternates between minor thirds (m3) and half steps (H). It has a unique, symmetrical structure that creates a sense of ambiguity and tension.</p>

<h2>Structure of the Augmented Scale</h2>
<p>The augmented scale alternates between minor thirds (m3) and half steps (H):</p>
<pre>
<code class="language-plaintext">
m3 - H - m3 - H - m3 - H
</code>
</pre>

<h2>Example: C Augmented Scale</h2>
<p>The C augmented scale consists of the following notes:</p>
<pre>
<code class="language-plaintext">
C - D♯ - E - G - G♯ - B - C
</code>
</pre>

<h2>Sound and Character</h2>
<p>The augmented scale has a unique, ambiguous sound due to its symmetrical structure. It is often used to create a sense of tension and instability, making it ideal for use in transitional passages and improvisation.</p>

<h2>Applications of the Augmented Scale</h2>
<p>The augmented scale is used in various musical contexts, including:</p>
<ul>
    <li>Melody: Creating ambiguous and tense melodies.</li>
    <li>Harmony: Adding tension and instability to chord progressions.</li>
    <li>Improvisation: Soloing with a unique, symmetrical sound.</li>
</ul>

<h2>Chords Derived from the Augmented Scale</h2>
<p>The augmented scale can be used to derive the following chords:</p>
<ul>
    <li>Augmented chords (e.g., C+)</li>
    <li>Augmented seventh chords (e.g., C7♯5)</li>
</ul>

