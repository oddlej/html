</code>
</pre>

<h2>Sound and Character</h2>
<p>The bebop scale has a smooth, flowing sound due to the added chromatic passing tone. It is often used in jazz improvisation to create lines that fit well with the harmonic structure of bebop music.</p>

<h2>Applications of the Bebop Scale</h2>
<p>The bebop scale is used in various musical contexts, including:</p>
<ul>
    <li>Melody: Creating smooth and flowing melodies.</li>
    <li>Harmony: Adding chromatic passing tones to chord progressions.</li>
    <li>Improvisation: Soloing with a smooth, bebop sound.</li>
</ul>

<h2>Chords Derived from the Bebop Scale</h2>
<p>The bebop scale can be used to derive the following chords:</p>
<ul>
    <li>Major chords (e.g., C)</li>
    <li>Dominant seventh chords (e.g., G7)</li>
    <li>Minor chords (e.g., Am)</li>
</ul>

