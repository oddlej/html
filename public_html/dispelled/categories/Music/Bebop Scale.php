<h1>Bebop Scale</h1>
<p>The bebop scale is an eight-note scale that is commonly used in jazz improvisation. It is derived from the major scale with an added chromatic passing tone, giving it a smooth, flowing sound that fits well with the harmonic structure of bebop music.</p>

<h2>Structure of the Bebop Scale</h2>
<p>The bebop scale follows the pattern of whole steps (W), half steps (H), and chromatic passing tones (P):</p>
<pre>
<code class="language-plaintext">
W - W - H - W - H - P - H - W
</code>
</pre>

<h2>Example: C Bebop Scale</h2>
<p>The C bebop scale consists of the following notes:</p>
<pre>
<code class="language-plaintext">
