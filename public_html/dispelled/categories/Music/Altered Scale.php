<h1>Altered Scale</h1>
<p>The altered scale, also known as the super Locrian scale, is a seven-note scale that is derived from the melodic minor scale. It is used to create tension and dissonance in jazz and fusion music, often over dominant seventh chords.</p>

<h2>Structure of the Altered Scale</h2>
<p>The altered scale follows the pattern of whole steps (W) and half steps (H) as follows:</p>
<pre>
<code class="language-plaintext">
H - W - H - W - W - W - W
</code>
</pre>

<h2>Example: G Altered Scale</h2>
<p>The G altered scale consists of the following notes:</p>
<pre>
<code class="language-plaintext">
G - A♭ - B♭ - B - D♭ - E♭ - F - G
</code>
</pre>

<h2>Sound and Character</h2>
<p>The altered scale has a tense and dissonant sound due to the presence of multiple altered tones. It is often used to create a sense of tension and resolution in jazz and fusion music.</p>

<h2>Applications of the Altered Scale</h2>
<p>The altered scale is used in various musical contexts, including:</p>
<ul>
    <li>Melody: Creating tense and dissonant melodies.</li>
    <li>Harmony: Adding tension and resolution to chord progressions.</li>
    <li>Improvisation: Soloing with a tense, altered sound.</li>
</ul>

<h2>Chords Derived from the Altered Scale</h2>
<p>The altered scale can be used to derive the following chords:</p>
<ul>
    <li>Altered dominant seventh chords (e.g., G7♯9, G7♭9, G7♯5, G7♭5)</li>
</ul>

