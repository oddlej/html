<h1>Tritone Scale</h1>
<p>The tritone scale, also known as the symmetrical diminished scale, is a six-note scale that alternates between whole steps (W) and half steps (H). It has a unique, symmetrical structure that creates a sense of tension and ambiguity in music.</p>

<h2>Structure of the Tritone Scale</h2>
<p>The tritone scale alternates between whole steps (W) and half steps (H):</p>
<pre>
<code class="language-plaintext">
W - H - W - H - W - H
</code>
</pre>

<h2>Example: C Tritone Scale</h2>
<p>The C tritone scale consists of the following notes:</p>
<pre>
<code class="language-plaintext">
C - D - E♭ - F - G - A♭ - C
</code>
</pre>

<h2>Sound and Character</h2>
<p>The tritone scale has a unique, symmetrical sound due to its alternating whole and half steps. It is often used to create a sense of tension and ambiguity in music.</p>

<h2>Applications of the Tritone Scale</h2>
<p>The tritone scale is used in various musical contexts, including:</p>
<ul>
    <li>Melody: Creating tense and ambiguous melodies.</li>
    <li>Harmony: Adding tension and complexity to chord progressions.</li>
    <li>Improvisation: Soloing with a unique, symmetrical sound.</li>
</ul>

<h2>Chords Derived from the Tritone Scale</h2>
<p>The tritone scale can be used to derive the following chords:</p>
<ul>
    <li>Diminished chords (e.g., C°)</li>
    <li>Half-diminished chords (e.g., Cm7♭5)</li>
    <li>Augmented chords (e.g., E+)</li>
</ul>

