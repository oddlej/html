<h1>Chord Progressions</h1>
<p>In music theory, a chord progression is a sequence of chords played in a specific order. Chord progressions are the foundation of harmony in music and are used to create the structure and movement of a piece.</p>

<h2>Common Chord Progressions</h2>
<p>There are several common chord progressions used in various genres of music. Here are a few examples:</p>

<h3>I-IV-V Progression</h3>
<p>The I-IV-V progression is one of the most common chord progressions in Western music. It consists of the first (I), fourth (IV), and fifth (V) chords of a major scale.</p>
<pre>
<code class="language-plaintext">
Example in C Major: C - F - G
</code>
</pre>

<h3>ii-V-I Progression</h3>
<p>The ii-V-I progression is commonly used in jazz and classical music. It consists of the second (ii), fifth (V), and first (I) chords of a major scale.</p>
<pre>
<code class="language-plaintext">
Example in C Major: Dm - G - C
</code>
</pre>

<h3>vi-IV-I-V Progression</h3>
<p>The vi-IV-I-V progression is often used in pop and rock music. It consists of the sixth (vi), fourth (IV), first (I), and fifth (V) chords of a major scale.</p>
<pre>
<code class="language-plaintext">
Example in C Major: Am - F - C - G
</code>
</pre>

