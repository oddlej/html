<h1>Hungarian Minor Scale</h1>
<p>The Hungarian minor scale is a seven-note scale that has a unique, exotic sound. It is characterized by its augmented second and raised fourth degree, and is often used in Hungarian, Eastern European, and gypsy music.</p>

<h2>Structure of the Hungarian Minor Scale</h2>
<p>The Hungarian minor scale follows the pattern of whole steps (W), half steps (H), and augmented seconds (A):</p>
<pre>
<code class="language-plaintext">
W - H - A - H - H - A - H
</code>
</pre>

<h2>Example: A Hungarian Minor Scale</h2>
<p>The A Hungarian minor scale consists of the following notes:</p>
<pre>
<code class="language-plaintext">
A - B - C - D# - E - F - G# - A
</code>
</pre>

<h2>Sound and Character</h2>
<p>The Hungarian minor scale has a unique, exotic sound due to its augmented second and raised fourth degree. It is often used to create a sense of mystery and drama in music.</p>

