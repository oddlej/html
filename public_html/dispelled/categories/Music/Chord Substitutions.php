<h2>Voice Leading with Chord Substitutions</h2>
<p>Voice leading is crucial when using chord substitutions. Here are some tips for smooth voice leading:</p>
<ul>
    <li><strong>Minimize Movement:</strong> Move individual notes by the smallest possible interval to create smooth transitions.</li>
    <li><strong>Common Tones:</strong> Keep common tones between chords to create a sense of continuity.</li>
    <li><strong>Contrary Motion:</strong> Use contrary motion (moving voices in opposite directions) to create interesting and balanced lines.</li>
</ul>

