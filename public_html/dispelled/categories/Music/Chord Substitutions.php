<h1>Chord Substitutions in Chord Melody</h1>
<p>Chord substitutions are a powerful tool in chord melody arrangements. They allow you to add variety, complexity, and color to your playing by replacing standard chords with alternative chords that serve the same harmonic function.</p>

<h2>Introduction to Chord Substitutions</h2>
<p>Chord substitutions involve replacing a chord with another chord that has a similar harmonic function. This can create new harmonic possibilities and add interest to your chord melody arrangements. Common types of chord substitutions include diatonic substitutions, tritone substitutions, and diminished substitutions.</p>

<h2>Diatonic Substitutions</h2>
<p>Diatonic substitutions involve replacing a chord with another chord from the same key. Here are some common diatonic substitutions:</p>
<ul>
    <li><strong>Relative Minor:</strong> Replace a major chord with its relative minor (e.g., C major with A minor).</li>
    <li><strong>Relative Major:</strong> Replace a minor chord with its relative major (e.g., A minor with C major).</li>
    <li><strong>Secondary Dominants:</strong> Use secondary dominant chords to temporarily tonicize a chord (e.g., D7 as the V7 of G major).</li>
</ul>

<h2>Tritone Substitutions</h2>
<p>Tritone substitutions involve replacing a dominant seventh chord with another dominant seventh chord a tritone away. This creates a smooth voice leading and adds tension to the progression. For example, G7 can be replaced with D♭7.</p>

<h2>Diminished Substitutions</h2>
<p>Diminished substitutions involve using diminished chords to create smooth voice leading and add tension. A common diminished substitution is to replace a dominant seventh chord with a diminished chord a half step higher. For example, G7 can be replaced with A♭°.</p>

