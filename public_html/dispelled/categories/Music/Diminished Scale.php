<h1>Diminished Scale</h1>
<p>The diminished scale, also known as the octatonic scale, is an eight-note scale that alternates between whole steps (W) and half steps (H). It is used to create tension and dissonance in music, often in jazz and classical compositions.</p>

<h2>Structure of the Diminished Scale</h2>
<p>The diminished scale alternates between whole steps (W) and half steps (H):</p>
<pre>
<code class="language-plaintext">
H - W - H - W - H - W - H - W
</code>
</pre>

<h2>Example: C Diminished Scale</h2>
<p>The C diminished scale consists of the following notes:</p>
<pre>
<code class="language-plaintext">
C - D♭ - E♭ - E - F# - G - A - B♭ - C
</code>
</pre>

<h2>Sound and Character</h2>
<p>The diminished scale has a tense and dissonant sound, characterized by its alternating whole and half steps. It is often used to create a sense of tension and instability, making it ideal for use in transitional passages and improvisation.</p>

<h2>Applications of the Diminished Scale</h2>
<p>The diminished scale is used in various musical contexts, including:</p>
<ul>
    <li>Melody: Creating tense and dissonant melodies.</li>
    <li>Harmony: Adding tension and dissonance to chord progressions.</li>
    <li>Improvisation: Soloing with a unique, dissonant sound.</li>
</ul>

<h2>Chords Derived from the Diminished Scale</h2>
<p>The diminished scale can be used to derive the following chords:</p>
<ul>
    <li>Diminished chords (e.g., C°)</li>
    <li>Half-diminished chords (e.g., Cm7♭5)</li>
    <li>Diminished seventh chords (e.g., Cdim7)</li>
</ul>

