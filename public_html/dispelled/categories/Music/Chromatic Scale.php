<h1>Chromatic Scale</h1>
<p>The chromatic scale is a twelve-note scale that includes all the pitches within an octave. It is used in various musical contexts to create tension, color, and complexity.</p>

<h2>Structure of the Chromatic Scale</h2>
<p>The chromatic scale consists entirely of half steps (H):</p>
<pre>
<code class="language-plaintext">
H - H - H - H - H - H - H - H - H - H - H - H
</code>
</pre>

<h2>Example: C Chromatic Scale</h2>
<p>The C chromatic scale consists of the following notes:</p>
<pre>
<code class="language-plaintext">
C - C# - D - D# - E - F - F# - G - G# - A - A# - B - C
</code>
</pre>

<h2>Sound and Character</h2>
<p>The chromatic scale has a tense and complex sound, characterized by the continuous sequence of half steps. It is often used in classical, jazz, and contemporary music to create a sense of movement and tension.</p>

<h2>Applications of the Chromatic Scale</h2>
<p>The chromatic scale is used in various musical contexts, including:</p>
<ul>
    <li>Melody: Creating tension and complexity in melodies.</li>
    <li>Harmony: Adding color and movement to chord progressions.</li>
    <li>Improvisation: Soloing with chromatic passing tones and embellishments.</li>
</ul>

<h2>Chords Derived from the Chromatic Scale</h2>
<p>The chromatic scale itself does not directly derive chords, but it can be used to add chromatic passing tones and embellishments to existing chords and progressions.</p>

