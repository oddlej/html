<h1>Whole Tone Scale</h1>
<p>The whole tone scale is a six-note scale in which each note is a whole step (W) apart. It has a unique, ambiguous sound that is often used to create a sense of dreaminess or otherworldliness in music.</p>

<h2>Structure of the Whole Tone Scale</h2>
<p>The whole tone scale consists entirely of whole steps (W):</p>
<pre>
<code class="language-plaintext">
W - W - W - W - W - W
</code>
</pre>

<h2>Example: C Whole Tone Scale</h2>
<p>The C whole tone scale consists of the following notes:</p>
<pre>
<code class="language-plaintext">
C - D - E - F# - G# - A# - C
</code>
</pre>

<h2>Sound and Character</h2>
<p>The whole tone scale has a unique, ambiguous sound due to the lack of half steps and the equal spacing of the notes. It is often used in impressionistic music to create a sense of dreaminess or otherworldliness.</p>

<h2>Applications of the Whole Tone Scale</h2>
<p>The whole tone scale is used in various musical contexts, including:</p>
<ul>
    <li>Melody: Creating dreamy and otherworldly melodies.</li>
    <li>Harmony: Adding ambiguity and color to chord progressions.</li>
    <li>Improvisation: Soloing with a unique, impressionistic sound.</li>
</ul>

<h2>Chords Derived from the Whole Tone Scale</h2>
<p>The whole tone scale can be used to derive the following chords:</p>
<ul>
    <li>Augmented chords (e.g., C+)</li>
    <li>Whole tone chords (e.g., C - D - E - F# - G# - A#)</li>
</ul>

