<h1>Circle of Fifths</h1>
<p>The Circle of Fifths is a visual representation of the relationships between the 12 tones of the chromatic scale, their corresponding key signatures, and the associated major and minor keys. It is a useful tool for understanding key signatures, chord progressions, and modulation.</p>

<h2>Structure of the Circle of Fifths</h2>
<p>The Circle of Fifths is arranged in a circular pattern, with each note a perfect fifth apart from the next. Starting from C at the top, the sequence of notes is:</p>
<pre>
<code class="language-plaintext">
C - G - D - A - E - B - F# - C# - Ab - Eb - Bb - F - C
</code>
</pre>

<h2>Key Signatures</h2>
<p>The Circle of Fifths also shows the key signatures for each major key. Moving clockwise, each key adds one sharp to the key signature. Moving counterclockwise, each key adds one flat to the key signature.</p>

<h3>Sharps</h3>
<pre>
<code class="language-plaintext">
G Major: 1 sharp (F#)
D Major: 2 sharps (F#, C#)
A Major: 3 sharps (F#, C#, G#)
E Major: 4 sharps (F#, C#, G#, D#)
B Major: 5 sharps (F#, C#, G#, D#, A#)
F# Major: 6 sharps (F#, C#, G#, D#, A#, E#)
C# Major: 7 sharps (F#, C#, G#, D#, A#, E#, B#)
</code>
</pre>

<h3>Flats</h3>
<pre>
<code class="language-plaintext">
F Major: 1 flat (Bb)
Bb Major: 2 flats (Bb, Eb)
Eb Major: 3 flats (Bb, Eb, Ab)
Ab Major: 4 flats (Bb, Eb, Ab, Db)
Db Major: 5 flats (Bb, Eb, Ab, Db, Gb)
Gb Major: 6 flats (Bb, Eb, Ab, Db, Gb, Cb)
Cb Major: 7 flats (Bb, Eb, Ab, Db, Gb, Cb, Fb)
</code>
</pre>

<h2>Relative Minor Keys</h2>
<p>Each major key has a relative minor key that shares the same key signature. The relative minor key is located a minor third below the major key. For example:</p>
<pre>
<code class="language-plaintext">
C Major: A Minor
G Major: E Minor
D Major: B Minor
A Major: F# Minor
E Major: C# Minor
B Major: G# Minor
F# Major: D# Minor
C# Major: A# Minor
F Major: D Minor
Bb Major: G Minor
Eb Major: C Minor
Ab Major: F Minor
Db Major: Bb Minor
Gb Major: Eb Minor
Cb Major: Ab Minor
</code>
</pre>

<h2>Applications of the Circle of Fifths</h2>
<p>The Circle of Fifths is used in various aspects of music, including:</p>
<ul>
    <li>Understanding key signatures and their relationships.</li>
    <li>Building chord progressions and modulating between keys.</li>
    <li>Composing and arranging music.</li>
</ul>

<h2>Practice Exercises</h2>
<p>Here are some practice exercises to help you get familiar with the Circle of Fifths:</p>
<ol>
    <li>Identify the key signature for E major and its relative minor key.</li>
    <li>Write out the Circle of Fifths and label the major and minor keys.</li>
    <li>Practice playing chord progressions that modulate between keys using the Circle of Fifths.</li>
</ol>

