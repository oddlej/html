<h1>Intervals</h1>
<p>In music theory, an interval is the distance between two notes. Intervals are fundamental to understanding the structure of scales, chords, and melodies.</p>

<h2>Types of Intervals</h2>
<p>Intervals can be classified into two main types:</p>
<ul>
    <li><strong>Melodic Intervals</strong>: The notes are played in sequence, one after the other.</li>
    <li><strong>Harmonic Intervals</strong>: The notes are played simultaneously.</li>
</ul>

<h2>Interval Names</h2>
<p>Intervals are named based on the number of letter names they encompass and their quality. The basic interval names are:</p>
<ul>
    <li><strong>Unison</strong>: The same note.</li>
    <li><strong>Second</strong>: Two letter names apart.</li>
    <li><strong>Third</strong>: Three letter names apart.</li>
    <li><strong>Fourth</strong>: Four letter names apart.</li>
    <li><strong>Fifth</strong>: Five letter names apart.</li>
    <li><strong>Sixth</strong>: Six letter names apart.</li>
    <li><strong>Seventh</strong>: Seven letter names apart.</li>
    <li><strong>Octave</strong>: Eight letter names apart.</li>
</ul>

<h2>Interval Qualities</h2>
<p>Intervals also have qualities that describe their size. The main interval qualities are:</p>
<ul>
    <li><strong>Perfect (P)</strong>: Unison, fourth, fifth, and octave.</li>
    <li><strong>Major (M)</strong>: Second, third, sixth, and seventh.</li>
    <li><strong>Minor (m)</strong>: One half step smaller than major intervals.</li>
    <li><strong>Augmented (A)</strong>: One half step larger than perfect or major intervals.</li>
    <li><strong>Diminished (d)</strong>: One half step smaller than perfect or minor intervals.</li>
</ul>

