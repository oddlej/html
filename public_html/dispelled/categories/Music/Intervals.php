<h2>Applications of Intervals</h2>
<p>Intervals are used in various aspects of music, including:</p>
<ul>
    <li>Melody: Creating melodic lines and themes.</li>
    <li>Harmony: Building chords and chord progressions.</li>
    <li>Ear Training: Developing the ability to recognize and sing intervals.</li>
</ul>

<h2>Practice Exercises</h2>
<p>Here are some practice exercises to help you get familiar with intervals:</p>
<ol>
    <li>Identify the interval between the notes C and A.</li>
    <li>Play a perfect fourth interval on your instrument, starting from G.</li>
    <li>Write out the notes of a minor sixth interval, starting from E.</li>
</ol>

