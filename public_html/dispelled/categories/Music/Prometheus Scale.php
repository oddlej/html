<h1>Prometheus Scale</h1>
<p>The Prometheus scale, also known as the mystic chord, is a six-note scale that was invented by Alexander Scriabin. It has a unique, mystical sound and is often used to create a sense of mystery and otherworldliness in music.</p>

<h2>Structure of the Prometheus Scale</h2>
<p>The Prometheus scale follows the pattern of whole steps (W) and minor thirds (m3):</p>
<pre>
<code class="language-plaintext">
W - W - W - m3 - W - W
</code>
</pre>

