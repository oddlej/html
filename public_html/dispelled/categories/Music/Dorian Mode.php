<h1>Dorian Mode</h1>
<p>The Dorian mode is the second mode of the major scale. It has a minor quality with a unique raised sixth degree, giving it a distinctive sound that is both melancholic and hopeful.</p>

<h2>Structure of the Dorian Mode</h2>
<p>The Dorian mode follows the pattern of whole steps (W) and half steps (H) as follows:</p>
<pre>
<code class="language-plaintext">
W - H - W - W - W - H - W
</code>
</pre>

<h2>Example: D Dorian Mode</h2>
<p>The D Dorian mode consists of the following notes:</p>
<pre>
<code class="language-plaintext">
D - E - F - G - A - B - C - D
</code>
</pre>

<h2>Sound and Character</h2>
<p>The Dorian mode has a minor quality with a raised sixth degree, giving it a unique sound that is both melancholic and hopeful. It is often used in jazz, blues, and folk music. The minor third and major sixth intervals contribute to its distinctive character.</p>

<h2>Applications of the Dorian Mode</h2>
<p>The Dorian mode is used in various musical contexts, including:</p>
<ul>
    <li>Melody: Creating melancholic yet hopeful melodies.</li>
    <li>Harmony: Building minor chords with a raised sixth degree.</li>
    <li>Improvisation: Soloing over minor chord progressions with a raised sixth degree.</li>
</ul>

