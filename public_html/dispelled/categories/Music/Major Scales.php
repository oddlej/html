<h1>Major Scales</h1>
<p>In music theory, a major scale is a diatonic scale that consists of seven distinct notes, plus an eighth note that is the same as the first note but an octave higher. The major scale is one of the most commonly used scales in Western music.</p>

<h2>Structure of a Major Scale</h2>
<p>The major scale follows a specific pattern of whole steps (W) and half steps (H). The pattern is:</p>
<pre>
<code class="language-plaintext">
W - W - H - W - W - W - H
</code>
</pre>
<p>This means that starting from the root note, you move up by a whole step, another whole step, a half step, three whole steps, and finally a half step to complete the octave.</p>

<h2>Example: C Major Scale</h2>
<p>The C major scale is one of the simplest major scales because it consists of all the natural notes (no sharps or flats). The notes in the C major scale are:</p>
<pre>
<code class="language-plaintext">
C - D - E - F - G - A - B - C
</code>
</pre>

<h2>Other Major Scales</h2>
<p>Here are some other major scales with their notes:</p>

<h3>G Major Scale</h3>
<pre>
<code class="language-plaintext">
G - A - B - C - D - E - F# - G
</code>
</pre>

<h3>D Major Scale</h3>
<pre>
<code class="language-plaintext">
D - E - F# - G - A - B - C# - D
</code>
</pre>

<h3>A Major Scale</h3>
<pre>
<code class="language-plaintext">
A - B - C# - D - E - F# - G# - A
</code>
</pre>

<h2>Applications of Major Scales</h2>
<p>Major scales are used in various aspects of music, including:</p>
<ul>
    <li>Melody: Creating melodies and themes.</li>
    <li>Harmony: Building chords and chord progressions.</li>
    <li>Improvisation: Providing a framework for improvisation.</li>
</ul>

<h2>Practice Exercises</h2>
<p>Here are some practice exercises to help you get familiar with major scales:</p>
<ol>
    <li>Play the C major scale on your instrument, ascending and descending.</li>
    <li>Write out the notes of the G major scale and play it on your instrument.</li>
    <li>Practice playing the D major scale, focusing on the correct fingerings.</li>
</ol>

