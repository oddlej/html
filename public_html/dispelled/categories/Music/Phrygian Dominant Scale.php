<h1>Phrygian Dominant Scale</h1>
<p>The Phrygian dominant scale, also known as the Spanish Phrygian scale, is a seven-note scale that is derived from the harmonic minor scale. It has an exotic, Middle Eastern sound and is often used in flamenco, jazz, and metal music.</p>

<h2>Structure of the Phrygian Dominant Scale</h2>
<p>The Phrygian dominant scale follows the pattern of whole steps (W) and half steps (H) as follows:</p>
<pre>
<code class="language-plaintext">
H - W+H - H - W - H - W - W
</code>
</pre>

