<h1>Aeolian Mode</h1>
<p>The Aeolian mode is the sixth mode of the major scale and is identical to the natural minor scale. It has a sad and melancholic sound, making it one of the most commonly used minor modes in Western music.</p>

<h2>Structure of the Aeolian Mode</h2>
<p>The Aeolian mode follows the pattern of whole steps (W) and half steps (H) as follows:</p>
<pre>
<code class="language-plaintext">
W - H - W - W - H - W - W
</code>
</pre>

<h2>Example: A Aeolian Mode</h2>
<p>The A Aeolian mode consists of the following notes:</p>
<pre>
<code class="language-plaintext">
A - B - C - D - E - F - G - A
</code>
</pre>

<h2>Sound and Character</h2>
<p>The Aeolian mode has a sad and melancholic sound, characterized by the minor third, minor sixth, and minor seventh intervals. It is often used in classical, rock, and pop music to convey emotions of sadness and introspection.</p>

<h2>Applications of the Aeolian Mode</h2>
<p>The Aeolian mode is used in various musical contexts, including:</p>
<ul>
    <li>Melody: Creating sad and melancholic melodies.</li>
    <li>Harmony: Building minor chords and chord progressions.</li>
    <li>Improvisation: Soloing over minor chord progressions.</li>
</ul>

