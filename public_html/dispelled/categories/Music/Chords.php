<h1>Chords</h1>
<p>In music theory, a chord is a group of notes played together, typically consisting of three or more notes. Chords are the building blocks of harmony and are used to create chord progressions and accompaniment.</p>

<h2>Types of Chords</h2>
<p>There are several types of chords, including:</p>
<ul>
    <li><strong>Major Chords</strong></li>
    <li><strong>Minor Chords</strong></li>
    <li><strong>Diminished Chords</strong></li>
    <li><strong>Augmented Chords</strong></li>
    <li><strong>Seventh Chords</strong></li>
</ul>

<h2>Major Chords</h2>
<p>A major chord consists of a root note, a major third, and a perfect fifth. The formula for a major chord is:</p>
<pre>
<code class="language-plaintext">
Root - Major Third - Perfect Fifth
</code>
</pre>
<p>Example: C Major Chord</p>
<pre>
<code class="language-plaintext">
C - E - G
</code>
</pre>

<h2>Minor Chords</h2>
<p>A minor chord consists of a root note, a minor third, and a perfect fifth. The formula for a minor chord is:</p>
<pre>
<code class="language-plaintext">
Root - Minor Third - Perfect Fifth
</code>
</pre>
<p>Example: A Minor Chord</p>
<pre>
<code class="language-plaintext">
A - C - E
</code>
</pre>

<h2>Diminished Chords</h2>
<p>A diminished chord consists of a root note, a minor third, and a diminished fifth. The formula for a diminished chord is:</p>
<pre>
<code class="language-plaintext">
Root - Minor Third - Diminished Fifth
</code>
</pre>
<p>Example: B Diminished Chord</p>
<pre>
<code class="language-plaintext">
B - D - F
</code>
</pre>

<h2>Augmented Chords</h2>
<p>An augmented chord consists of a root note, a major third, and an augmented fifth. The formula for an augmented chord is:</p>
<pre>
<code class="language-plaintext">
Root - Major Third - Augmented Fifth
</code>
</pre>
<p>Example: C Augmented Chord</p>
<pre>
<code class="language-plaintext">
C - E - G#
</code>
</pre>

<h2>Seventh Chords</h2>
<p>Seventh chords add a seventh note to the basic triad. There are several types of seventh chords, including:</p>
<ul>
    <li><strong>Major Seventh Chord</strong>: Root - Major Third - Perfect Fifth - Major Seventh</li>
    <li><strong>Minor Seventh Chord</strong>: Root - Minor Third - Perfect Fifth - Minor Seventh</li>
    <li><strong>Dominant Seventh Chord</strong>: Root - Major Third - Perfect Fifth - Minor Seventh</li>
</ul>

<h2>Applications of Chords</h2>
<p>Chords are used in various aspects of music, including:</p>
<ul>
    <li>Harmony: Creating harmonic progressions and accompaniment.</li>
    <li>Composition: Building the harmonic structure of a piece.</li>
    <li>Improvisation: Providing a framework for improvisation.</li>
</ul>

<h2>Practice Exercises</h2>
<p>Here are some practice exercises to help you get familiar with chords:</p>
<ol>
    <li>Play the C major chord on your instrument.</li>
    <li>Write out the notes of the G minor chord and play it on your instrument.</li>
    <li>Practice playing the F diminished chord, focusing on the correct fingerings.</li>
</ol>

