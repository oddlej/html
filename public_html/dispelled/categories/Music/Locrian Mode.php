<h1>Locrian Mode</h1>
<p>The Locrian mode is the seventh mode of the major scale. It has a dark and unstable sound, characterized by a diminished fifth interval that gives it a distinctive, dissonant quality.</p>

<h2>Structure of the Locrian Mode</h2>
<p>The Locrian mode follows the pattern of whole steps (W) and half steps (H) as follows:</p>
<pre>
<code class="language-plaintext">
H - W - W - H - W - W - W
</code>
</pre>

<h2>Example: B Locrian Mode</h2>
<p>The B Locrian mode consists of the following notes:</p>
<pre>
<code class="language-plaintext">
B - C - D - E - F - G - A - B
</code>
</pre>

<h2>Sound and Character</h2>
<p>The Locrian mode has a dark and unstable sound, characterized by the diminished fifth interval. It is often used in jazz, metal, and experimental music to create a sense of tension and dissonance. The minor second, minor third, and diminished fifth intervals contribute to its distinctive character.</p>

<h2>Applications of the Locrian Mode</h2>
<p>The Locrian mode is used in various musical contexts, including:</p>
<ul>
    <li>Melody: Creating dark and dissonant melodies.</li>
    <li>Harmony: Building diminished chords and chord progressions.</li>
    <li>Improvisation: Soloing over diminished chord progressions.</li>
</ul>

