<h2>Techniques for Integrating Counterpoint</h2>
<p>Here are some techniques for integrating counterpoint into your chord melody arrangements:</p>
<ul>
    <li><strong>Contrary Motion:</strong> Move the counterpoint line in the opposite direction of the melody to create balance and interest.</li>
    <li><strong>Parallel Motion:</strong> Use parallel motion sparingly to create moments of unity between the voices.</li>
    <li><strong>Oblique Motion:</strong> Keep one voice stationary while the other moves to create stability and contrast.</li>
    <li><strong>Imitative Counterpoint:</strong> Repeat a melodic idea in another voice to create cohesion and variation.</li>
    <li><strong>Harmonic Intervals:</strong> Use harmonic intervals such as thirds, sixths, and tenths to create consonance between the voices.</li>
</ul>

