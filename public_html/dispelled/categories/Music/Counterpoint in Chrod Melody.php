<h1>Counterpoint in Chord Melody</h1>
<p>Counterpoint is the art of combining independent melodic lines to create harmony. Incorporating counterpoint into chord melody arrangements can add complexity, depth, and interest to your playing. This tutorial will explore techniques for integrating counterpoint with chord melody.</p>

<h2>Introduction to Counterpoint</h2>
<p>Counterpoint involves the interaction of two or more independent melodic lines. These lines, or voices, move in a way that creates harmony while maintaining their individuality. In chord melody, counterpoint can be used to create intricate and engaging arrangements.</p>

<h2>Basic Principles of Counterpoint</h2>
<p>Here are some basic principles of counterpoint to get you started:</p>
<ul>
    <li><strong>Contrary Motion:</strong> Move the voices in opposite directions to create balance and interest.</li>
    <li><strong>Parallel Motion:</strong> Move the voices in the same direction by the same interval. Use sparingly to avoid monotony.</li>
    <li><strong>Oblique Motion:</strong> Keep one voice stationary while the other moves. This creates a sense of stability and contrast.</li>
    <li><strong>Imitative Counterpoint:</strong> Repeat a melodic idea in another voice, either exactly or with variation.</li>
</ul>

