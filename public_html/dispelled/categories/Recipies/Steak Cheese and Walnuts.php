<h1>Yellow Peppers with Steak Cheese and Walnuts</h1>

<p>Requires: Steak Cheese</p>

<p>Preheat oven or barbecue to 375 degrees</p>

<pre>4 yellow peppers
8 mushrooms
1/2 cup of frozen spinach
2 heaping tsp. of garlic puree
1 1/2 cups shelled walnuts</pre>

<p>Preparation:

<p>Cut 4 yellow peppers in half and put in casserole dish (or foil for barbecue)</p>
<p>Slice mushroom into 4 and put in pepper half</p>
<p>Put a layer of spinach on each pepper half</p>
<p>Put a bit of garlic in the center of each pepper half</p>
<p>Add steak cheese to each pepper half until almost full</p>
<p>Add shelled walnuts to each pepper half</p>
<p>Sprinkle a bit of pepper on top</p>
<p>Cover casserole dish (or cover with foil on top rack of barbecue) and bake for 15 minutes at 375 degrees.</p>

