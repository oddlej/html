<h1>Beer Caesars Part 2</h1>
<p>This refreshing drink combines the flavors of beer and Caesar cocktail. It's perfect for a summer day or a casual get-together.</p>

<h2>Ingredients</h2>
<ul>
    <li>1 bottle of beer</li>
    <li>1 shot of vodka</li>
    <li>1 cup Clamato juice</li>
    <li>1 lime wedge</li>
    <li>Salt and pepper to taste</li>
</ul>

<h2>Instructions</h2>
<pre>
<code class="language-plaintext">
1. Pour the beer into a large glass.
2. Add the shot of vodka.
3. Pour in the Clamato juice.
4. Squeeze the lime wedge into the glass.
5. Stir that all up with a fork and have a vodka shot first. Delicious.
6. Garnish with a lime wedge and enjoy!
</code>
</pre>

