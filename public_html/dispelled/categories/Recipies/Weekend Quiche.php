<h1>Weekend Quiche</h1>

<p>Preheat oven to 375 degrees</p>

<p>Prepare:</p>

<pre>1 cup chopped ham
1/2 cup of peas
10 oz shredded cheddar cheese
1 cup chopped onions (microwave for 30 seconds)</pre>

<p>Combine dry ingredients:</p>

<pre>1 cup of white flour
1 tsp. of baking powder
1/4 tsp. salt
3/4 cup parmesan cheese</pre>

<p>Combine wet ingredients:</p>

<pre>4 eggs
2 cups of milk
1/4 cup melted butter</pre>

<p>Add wet ingredients to dry ingredients, Add ham, onion, peas and most of cheese. Pour into lightly greased 10" round dish and sprinkle remaining cheese on top</p>

<p>Bake for 35 minutes and then until eggs are set and top is browned.</p>


