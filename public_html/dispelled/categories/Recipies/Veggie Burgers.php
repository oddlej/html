<h1>Veggie Burgers</h1>
								
<p>To be combined in food processor</p>

<pre>Prepare:
4 cups cooked or canned Chick Pea
2 cloves of Garlic mashed
1/2 an onion diced
1/2 jar of pickled beets
10 saltines crushed
1 tsp cumin
1 tsp onion powder
1 tsp Nashville hot chicken rub
Handful of fresh parsley
Handful of chives
3 eggs
1 tsp mustard
</pre>
<p>Put in food processor until a decent consistency is made, not too coarse, not too fine. Form into patties and put on baking sheet and freeze. Later wrap in saran for storage. <3 me Zoz!</p>

