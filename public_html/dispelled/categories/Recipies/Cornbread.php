<h1>Cornbread</h1>
								
<p>Preheat oven to 400 degrees</p>

<p>Combine dry ingredients:</p>

<pre>1 cup white flour
1 cup cornmeal
1/4 cup sugar
1/2 tsp. baking powder
1/2 tsp. baking soda
1/2 tsp. salt</pre>

<p>Combine wet ingredients:</p>

<pre>1 egg lightly beaten
1 cup sour cream
1/3 cup of milk
1/4 cup of melted butter</pre>

<p>Combine dry ingredients and wet ingredients</p>

<p>Pour into lightly greased 4"x8" loaf pan, cook for 15 minutes, and then cook until browned. For double recipe, pour into 8" square pan and cook for 20 minutes.</p>

