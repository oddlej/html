<h1>Candy Cane Biscotti</h1>
<p>This festive Candy Cane Biscotti recipe is perfect for the holiday season. Enjoy these crunchy, minty treats with a cup of coffee or hot chocolate.</p>

<h2>Ingredients</h2>
<ul>
    <li>1/2 cup Land O Lakes® Butter, softened</li>
    <li>3/4 cup sugar</li>
    <li>2 large Land O Lakes® Eggs</li>
    <li>1 teaspoon peppermint extract</li>
    <li>2 1/2 cups all-purpose flour</li>
    <li>1 teaspoon baking powder</li>
    <li>1/4 teaspoon salt</li>
    <li>1/2 cup crushed peppermint candies</li>
    <li>1/2 cup white chocolate chips</li>
    <li>1/2 cup semi-sweet chocolate chips</li>
</ul>

<h2>Instructions</h2>
<pre>
<code class="language-plaintext">
1. Heat oven to 350°F.

2. Combine butter and sugar in a large bowl. Beat at medium speed, scraping bowl often, until creamy. Add eggs and peppermint extract; continue beating until well mixed. Add flour, baking powder, and salt; beat at low speed until well mixed. Stir in crushed candies and white chocolate chips.

3. Divide dough in half. Shape each half into 10x3-inch log on a lightly floured surface. Place logs 4 inches apart onto a greased baking sheet; flatten each log to 1-inch thickness.

4. Bake for 18-25 minutes or until set and lightly browned. Cool on baking sheet for 10 minutes. Reduce oven temperature to 325°F.

5. Carefully remove logs from baking sheet; place on a cutting board. Cut each log into 1/2-inch slices with a serrated knife. Place slices, cut-side down, onto ungreased baking sheets. Bake for 15-18 minutes or until crisp. Cool completely.

6. Melt semi-sweet chocolate chips in a small microwave-safe bowl according to package directions. Drizzle over cooled biscotti. Let stand until chocolate is set.
</code>
</pre>

