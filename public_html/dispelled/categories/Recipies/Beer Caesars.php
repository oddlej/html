<h1>Beer Caesars</h1>

<p id="firstHeading" class="firstHeading" lang="en">So maybe my pictures don’t do it justice, but this is a damn fine drink. A slight variation on the ol’ Beer n Clam. Additions are pickles, pickle juice, pickled beans, <a href="https://en.wikipedia.org/wiki/Sriracha_sauce">sriracha sauce</a>, <a href="http://www.uvvodka.com/products/sriracha">sriracha vodka</a>, vodka, pepper, bacon rim spice, really whatever a regular caesar would get. This is my favorite.</p>
<pre>1 Beer (Coors Light for me)
3 Ice cubes
Clamato to top
4 Banana pepper rings
Splash of Banana pepper juice 1/2 tsp Creamed Horseradish</pre>
<p>Stir that all up with a fork and have a vodka shot first. Delicious.</p>
