    <li>Determine the number of turns in the secondary coil of a transformer with a primary voltage of 120V, a secondary voltage of 60V, and a primary coil with 150 turns.</li>
</ol>

<h2>Types of Transformers</h2>
<p>There are several types of transformers, including:</p>
<ul>
    <li><strong>Step-Up Transformers</strong>: Increase the voltage from the primary to the secondary winding.</li>
    <li><strong>Step-Down Transformers</strong>: Decrease the voltage from the primary to the secondary winding.</li>
    <li><strong>Isolation Transformers</strong>: Provide electrical isolation between the primary and secondary windings.</li>
</ul>

<h2>Transformer Equations</h2>
<p>The primary and secondary voltages and currents in a transformer are related by the turns ratio (N1/N2):</p>
<pre>
<code class="language-plaintext">
V1 / V2 = N1 / N2
I1 / I2 = N2 / N1
</code>
</pre>
<p>Where:</p>
<ul>
    <li><strong>V1</strong> is the primary voltage</li>
    <li><strong>V2</strong> is the secondary voltage</li>
    <li><strong>I1</strong> is the primary current</li>
    <li><strong>I2</strong> is the secondary current</li>
    <li><strong>N1</strong> is the number of turns in the primary winding</li>
    <li><strong>N2</strong> is the number of turns in the secondary winding</li>
</ul>

<h2>Applications of Transformers</h2>
<p>Transformers are used in various applications, including:</p>
<ul>
    <li>Power Distribution: Transmitting electrical power over long distances.</li>
    <li>Voltage Regulation: Maintaining a constant voltage level.</li>
    <li>Isolation: Providing electrical isolation between circuits.</li>
</ul>

<h2>Example Circuit</h2>
<p>Here is an example of a simple step-down transformer circuit:</p>
<pre>
<code class="language-plaintext">
Components:
- Transformer (120V to 12V)
- Load resistor (10 ohms)
- Power supply (120V AC)

Circuit:
1. Connect the primary winding of the transformer to the 120V AC power supply.
2. Connect the secondary winding of the transformer to the load resistor.

The secondary voltage is given by:
V2 = V1 * (N2 / N1)
</code>
</pre>
