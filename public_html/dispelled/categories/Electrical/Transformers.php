<h1>Transformers</h1>
<p>Transformers are electrical devices that transfer electrical energy between two or more circuits through electromagnetic induction. They are essential for power distribution and voltage regulation.</p>

<h2>Transformer Formula</h2>
<p>The formula for a transformer is:</p>
<pre>
<code class="language-plaintext">
Vp/Vs = Np/Ns
</code>
</pre>
<p>Where:</p>
<ul>
    <li><strong>Vp</strong> is the primary voltage</li>
    <li><strong>Vs</strong> is the secondary voltage</li>
    <li><strong>Np</strong> is the number of turns in the primary coil</li>
    <li><strong>Ns</strong> is the number of turns in the secondary coil</li>
</ul>

<h2>Understanding Transformers</h2>
<p>Transformers consist of two coils of wire, the primary and secondary, wound around a magnetic core. When an AC voltage is applied to the primary coil, it creates a magnetic field that induces a voltage in the secondary coil.</p>

<h2>Example Calculation</h2>
<p>Consider a transformer with a primary voltage of 120V, a primary coil with 100 turns, and a secondary coil with 50 turns:</p>
<pre>
<code class="language-plaintext">
Vp/Vs = Np/Ns
120V/Vs = 100/50
Vs = 60V
</code>
</pre>

<h2>Applications of Transformers</h2>
<p>Transformers are used in:</p>
<ul>
    <li>Power distribution</li>
    <li>Voltage regulation</li>
    <li>Isolation of circuits</li>
</ul>

<h2>Practice Problems</h2>
