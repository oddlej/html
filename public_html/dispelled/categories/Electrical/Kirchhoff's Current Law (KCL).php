<h1>Kirchhoff's Current Law (KCL)</h1>
<p>Kirchhoff's Current Law (KCL) states that the sum of all currents entering a junction in a circuit is equal to the sum of all currents leaving the junction.</p>

<h2>KCL Formula</h2>
<p>The formula for KCL is:</p>
<pre>
<code class="language-plaintext">
∑I_in = ∑I_out
</code>
</pre>

<h2>Understanding KCL</h2>
<p>KCL is based on the principle of conservation of charge. It means that the total charge entering a junction must equal the total charge leaving the junction.</p>

<h2>Example Calculation</h2>
<p>Consider a junction with three currents:</p>
<pre>
<code class="language-plaintext">
I1 + I2 = I3
</code>
</pre>
<p>If I1 = 2A, I2 = 3A, and I3 = 5A, then:</p>
<pre>
<code class="language-plaintext">
2A + 3A = 5A
</code>
</pre>

<h2>Applications of KCL</h2>
<p>KCL is used in:</p>
<ul>
    <li>Analyzing complex circuits</li>
    <li>Designing electrical systems</li>
    <li>Troubleshooting circuit problems</li>
</ul>

